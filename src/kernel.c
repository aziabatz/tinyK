void tiny_logo(multiboot_info_t *multiboot)
{
    uint32 addr = multiboot->framebuffer_addr;
    uint32 width = multiboot->framebuffer_width;
    uint32 height = multiboot->framebuffer_height;
    uint32 pitch = multiboot->framebuffer_pitch;
    uint32 bpp = multiboot->framebuffer_bpp;
    uint32 pixelwidth = bpp / 8; // calculate pixel width in bytes

    // Define the 8x16 "A" in RGB format
    unsigned char letter_A[] = {
        255, 255, 255,   // white
        255,   0,   0,   // red
        255,   0,   0,   // red
        255,   0,   0,   // red
        255, 255, 255,   // white
        255,   0,   0,   // red
        255,   0,   0,   // red
        255,   0,   0,   // red
        255, 255, 255,   // white
        255, 255, 255,   // white
        255, 255, 255,   // white
        255, 255, 255,   // white
        255,   0,   0,   // red
        255,   0,   0,   // red
        255,   0,   0,   // red
        255, 255, 255    // white
    };

    int letter_width = 32;
    int letter_height = 32;

    int target_width = 500;
    int target_height = 500;

    // Loop over each pixel of the scaled letter and write it to the framebuffer
    for(int y = 0; y < target_height; y++) {
       for(int x = 0; x < target_width; x++) {
            // Calculate the corresponding pixel in the letter image
            int src_x = x * letter_width / target_width;
            int src_y = y * letter_height / target_height;

            // Calculate the framebuffer address for this pixel
            unsigned char *pixel = addr + (y+200)*pitch + (x+200)*pixelwidth;

            // Get the color value of this pixel from the letter array
            unsigned char *color = c_original_logo_icon_146611 + (src_y * letter_width + src_x) * 3;

            // Write the color values to the framebuffer
            *pixel = *(color + 2);         // blue component
            *(pixel + 1) = *(color + 1);   // green component
            *(pixel + 2) = *(color);       // red component
            //*(pixel + 3) = 0;              // alpha component (if present)
       }
       int a =0;
    }
    
}

int _kmain(pg_dir_t *kdir,
           multiboot_info_t *multiboot,
           uint32 magicnum,
           uint32 stack)
{
    driver_t *vga = install_vga();
    set_tty_dev(vga);

    tiny_logo(multiboot);
    
    for(;;);


    kinfo(INFO, "Bootstrap OK!");

    mboot_mmap_load(multiboot);

    mboot_show_mmap();

    // memory detection
    if (multiboot->mem_upper < (10 * 1024))
    {
        kinfo(WARNING, "Total system RAM is below 10Mib!");
    }
    kprintf("\t\tRecognized memory: low=%d(KB)    high=%d(KB)\n", multiboot->mem_lower, multiboot->mem_upper);

    // init system
    gdt_init();
    kinfo(INFO, "GDT Loaded");

    irq_init();
    kinfo(INFO, "IRQ is set");

    idt_init();
    kinfo(INFO, "IDT Loaded");
    pg_set_handler();
    kinfo(INFO, "Page Fault handler set");

    //timer_init();

    
    pm_mgr_init(NULL, multiboot->mem_lower + multiboot->mem_upper, kdir);

    size_t total, free;
    pm_mgr_get_status(&total, &free);

    heap_t * kheap = init_kheap(kdir, (PG_PTE_READ_WRITE | PG_PTE_PRESENT));

    virt_t allocs[5];
    for (size_t i = 0; i < 5; i++)
    {  
        
        allocs[i] = kmalloc(kheap, i * 2);
        if(allocs[i]){
        heap_block_t * hp = (uintptr_t)(allocs[i])-sizeof(heap_block_t);
        
        kprintf("reserved with malloc at 0x%x with:\n"
        "base:0x%x length:0x%x\n", hp, hp->base, hp->length);
        }
        else{
            kprintf("%s\n", "Error allocating");
        }
    }

    for (size_t i = 0; i < 5; i++)
    {
        if(kfree(kheap, allocs[i]))
            kprintf("freed with kfree chunk at 0x%x\n", allocs[i]);
        else
            kprintf("kfree thrown error freeing chunk at 0x%x\n", allocs[i]);
    }
    
    kdir = vm_clone_dir(kdir);

    //TODO Get kheap from scheduler
    set_kernel_heap(kheap);

    proc_t * procA = new_process(1, &task_A, true);
    proc_t * procB = new_process(2, &task_B, true);
    proc_t * procC = new_process(3, &task_C, true);

    init_sched(1, kheap);
    add_process(procA, "procA");
    add_process(procB, "procB");
    add_process(procC, "procC");

    
    set_bg(WHITE);set_fg(BLACK);
	timer_init();
    __hold_on();
}
